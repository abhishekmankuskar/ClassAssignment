package question4;

public class Rectangle {
	private float length, width;

	public Rectangle() {
		length = 1;
		width = 1;
	}

	public float getLength() {
		return length;
	}
	public void setLength(float length) {
		this.length = length;
	}
	public float getWidth() {
		return width;
	}
	public void setWidth(float width) {
		this.width = width;
	}

}